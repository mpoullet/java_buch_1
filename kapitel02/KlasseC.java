public class KlasseC {
// Konstruktordefinition
   KlasseC() {
   }
// Klassenmethode, die einen Text ausgibt 
   protected static void k() {
      System.out.println(
        "Die Klassenmethode der Oberklasse wurde aufgerufen");
   }
// Instanzmethode, die einen Text ausgibt 
   protected void m() {
      System.out.println(
        "Die Instanzmethode der Oberklasse wurde aufgerufen");
   }
}
